						<h1 class="page-title">Stress</h1>


						<p class="lead">Stress is a normal reaction the body has when changes occur, resulting in physical, emotional and intellectual responses. Stress management training can help you deal with changes in a healthier way.</p>

                  <h3>What is stress?</h3>
						<p>Stress is a normal human reaction that happens to everyone. In fact, the human body is designed to experience stress and react to it. When you experience changes or challenges (stressors), your body produces physical and mental responses. That’s stress.</p>
                  <p>Stress responses help your body adjust to new situations. Stress can be positive, keeping us alert, motivated and ready to avoid danger. For example, if you have an important test coming up, a stress response might help your body work harder and stay awake longer. But stress becomes a problem when stressors continue without relief or periods of relaxation.</p>

                  <h3>What happens to the body during stress?</h3>
                  <p>The body’s autonomic nervous system controls your heart rate, breathing, vision changes and more. Its built-in stress response, the “fight-or-flight response,” helps the body face stressful situations.</p>
                  <p>When a person has long-term (chronic) stress, continued activation of the stress response causes wear and tear on the body. Physical, emotional and behavioral symptoms develop.</p>
                  <ul>Physical symptoms of stress include:
                  <li>Aches and pains.</li>
                  <li>Chest pain or a feeling like your heart is racing.</li>
                  <li>Exhaustion or trouble sleeping.</li>
                  <li>Headaches, dizziness or shaking.</li>
                  <li>Muscle tension or jaw clenching.</li>
                  <li>Stomach or digestive problems.</li>
                  </ul>
                  <h3>How is stress diagnosed?</h3>
                  <p>Stress is subjective — not measurable with tests. Only the person experiencing it can determine whether it's present and how severe it feels. A healthcare provider may use questionnaires to understand your stress and how it affects your life.</p>
						<p>If you have chronic stress, your healthcare provider can evaluate symptoms that result from stress. For example, high blood pressure can be diagnosed and treated.</p>
