                        <h1 class="page-title">Social Media Harassment: 4 Types You Should Know</h1>

                        <ul class="entry-meta">
                            <li class="date">September 02, 2021, JEFF CHEN</li>
                        </ul>

                        <p class="lead">The terminology ‘social media harassment’, sometimes characterized as online
                            bullying, refers to the employment of the internet to stalk, intimidate, harm, or disgrace
                            someone. Abuse, harassing, trolls, flaming, and other forms of cyberbullying are all
                            examples of cyberbullying. Some words are interchangeable, while others have lost their
                            significance. Online abuse or online harassment, as defined by PEN America, is the
                            widespread or severe targeting of an individual or group online through destructive
                            actions. </p>


                        <p>In September, a Pew Research Center survey of U.S. citizens discovered that 41% of Americans
                            had directly encountered social media harassment in at least one of six major categories
                            studied.</p>

                        <h2>Types of Social Media Harassment</h2>
                        <ul>
                            <li>Concerned trolling</li>
                        </ul>
                        <p>Abusers impersonate fans or followers of an artist's work and send damaging and insulting
                            remarks disguised as constructive criticism. Counter-speech can be futile, and banning may
                            intensify the abuse since concern trolls are attempting to attract the eye and spend your
                            time. </p>
                        <p>It's possible that muting, which allows users to suppress certain offensive content, so
                            people wouldn't have to see it, is more helpful. Ensure careful to identify any information
                            that crosses the line from bothersome to abusive, and try enlisting the help of a supportive
                            online community. </p>
                        <ul>
                                <li><strong>Cyberstalking</strong></li>
                        </ul>
                        <p>In a strict context, "cyberstalking" refers to the employment of abusive behaviors
                            electronically over an extended period to murder, harm, threaten, terrorize through the
                            surveillance of a person online. Cyberstalking is a federal offense, and several countries
                            have laws against it.</p>
                        <p>One might choose to pursue legal action regarding a cyberstalker if they feel unsafe by
                            calling law enforcement or obtaining legal guidance. Other tactics include:</p>
                        <li>Banning your stalker on social networking sites.</li>
                        <li>Recording every annoying episode related to cyberstalking.</li>
                        <li>Ensuring your online accounts are secure if you suspect identity theft.</li>
                        <li>Seeking the help of your support network.</li>
                        <li><strong>Cyberbullying</strong></li>
                        <p>Cyberbullying is a broad word that refers to a variety of harassing activities. It is defined
                            as "willful and repetitive harm perpetrated through the use of computers, mobile phones, as
                            well as other electrical gadgets." The phrase is most commonly applied to children and young
                            people. For the most up-to-date materials and facts about cyberbullying, visit
                            Cyberbullying.org. </p>
                        <li><strong>Cyber-Mob Violence (Dogpiling)</strong></li>
                        <p>A big group of abusers uses a torrent of accusations, insults, obscenities, and other abusive
                            methods to assault a victim. Furthermore, 20% of Americans claim they have already been
                            bullied. Half of those who have been harassed digitally have just been victimized because of
                            their political opinions. Outrage mob action is a type of mob punishment that focuses on
                            publicly exposing, shaming, and penalizing a victim, usually for voicing thoughts on
                            politically hot issues or concepts that the outrage community disagrees with or has
                            misinterpreted to advance a specific goal. </p>
                        <p>Navigating cyber-mob assaults might feel like a never-ending battle of overdrive. If
                            disclosing the harassment isn't working, probably ask a representative of the support
                            network to keep an eye on it and notify you while users take a little break. Other
                            possibilities include conducting a counter-speech crusade to establish and maintain a
                            version of history or restore a Twitter handle associated with their screen name, issuing a
                            statement informing the networking site to the pessimistic interaction, and partially
                            removing or going confidential on the social media pages until it has settled.</p>
