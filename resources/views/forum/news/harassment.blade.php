                            the many types of social media harassment and attempt to avoid it. Companies should provide
                            training programs that teach employees about the many types of workplace social media abuse
                            and how to avoid it. Individuals must also become aware of the several alternatives
                            accessible to them in the face of cyberbullying. The following are the examples of
                            occupational social media harassment:</p>
                        <li>Stalking on the Internet</li>
                        <p>A circumstance in which a coworker harasses a colleague by keeping track of everything they
                            are doing on social media or through blog postings. A group of coworkers can also make a
                            blog article or a website to annoy other colleagues.</p>
                        <li>Harassment on the Internet</li>
                        <p>Whenever a company, manager, employee, or teammate uses social media sites like Facebook or
                            Twitter to send unpleasant remarks or texts to another coworker, this is known as virtual
                            harassment.</p>
                        <li>Harassment through text</li>
                        <p>When a coworker is intimidated, harassed, or sent improper remarks by another coworker.
                            Harassment in the form of sexting is a type of harassment. One individual sends video or
                            photographs to another person via digital communication, but the recipient is upset.</p>
